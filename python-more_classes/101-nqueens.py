#!/usr/bin/env python3
"""Module that solves the N-queens problem, its very hard my friend"""
import sys


def is_safe(board, row, col, n):
    """Check if a queen can be placed in a position on the board"""
    for i in range(col):
        if board[row][i] == 1:
            return False

    for i, j in zip(range(row, -1, -1), range(col, -1, -1)):
        if board[i][j] == 1:
            return False

    for i, j in zip(range(row, n, 1), range(col, -1, -1)):
        if board[i][j] == 1:
            return False

    return True


def solve_nqueens(board, col, n):
    """Solve the N-queens problem using backtracking"""
    if col >= n:
        print_solution(board)
        return True

    for i in range(n):
        if is_safe(board, i, col, n):
            board[i][col] = 1
            solve_nqueens(board, col + 1, n)
            board[i][col] = 0


def print_solution(board):
    """Print the solution in all cases"""
    solution = []
    for i in range(len(board)):
        for j in range(len(board)):
            if board[i][j] == 1:
                solution.append([i, j])
    print(solution)


def nqueens(n):
    """Solver for the N-queens problem using backtracking"""
    board = [[0 for _ in range(n)] for _ in range(n)]
    solve_nqueens(board, 0, n)


if __name__ == "__main__":
    if len(sys.argv) != 2:
        print("Usage: nqueens N")
        sys.exit(1)

    try:
        n = int(sys.argv[1])
    except ValueError:
        print("N must be a number")
        sys.exit(1)

    if n < 4:
        print("N must be at least 4")
        sys.exit(1)

    nqueens(n)
